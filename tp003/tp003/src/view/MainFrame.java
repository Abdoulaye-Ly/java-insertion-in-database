    private void afficherClient() {
        List<Client> lst = DaoFactory.getDaoClient().findAll();
        ((DefaultTableModel)tableClient.getModel()).setRowCount(lst.size());
        for(int i=0; i<lst.size(); i++){
            tableClient.setValueAt(lst.get(i).getId(), i, 0);
            tableClient.setValueAt(lst.get(i).getNom(), i, 1);
            tableClient.setValueAt(lst.get(i).getPrenom(), i, 2);
            tableClient.setValueAt(lst.get(i).getAdresse(), i, 3);
        }
    }
